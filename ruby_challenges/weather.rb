current_weather = 'sunny'

case current_weather
  when 'sunny'
    puts "Bust out the shorts!"
  when 'cloudy'
    puts "No need for the shades today"
  when 'rainy'
    puts "Bring the rain gear!"
  when 'cold'
    puts "Bring the warm coat today folks"
  else
    puts "We haven't had that kind of weather here in a long time!"
end
